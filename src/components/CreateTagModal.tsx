interface CreateTagModalProps {
  onClose: () => void;
  onCreateTag: (tagData: { key: string; label: string; iconName: string }) => void;
}

export function CreateTagModal({ onClose, onCreateTag }: CreateTagModalProps) {
  const { t } = useTranslation();
  const [tagName, setTagName] = useState("");
  const [selectedIcon, setSelectedIcon] = useState<string>("StarIcon");
  const [accentColor, setAccentColor] = useState("#007aff");

  useEffect(() => {
    invoke<string>("get_system_accent_color")
      .then((color) => setAccentColor(color))
      .catch(() => setAccentColor("#007aff"));
  }, []);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (tagName.trim()) {
      const key = tagName
        .toLowerCase()
        .replace(/\s+/g, "-")
        .replace(/[^a-z0-9-]/g, "");
      onCreateTag({
        key: `custom-${key}`,
        label: tagName.trim(),
        iconName: selectedIcon,
      });
      onClose();
    }
  };

  return (
    <Modal onClose={onClose} title={t("createTag.title", "Create Custom Tag")} maxWidth="450px">
      <form onSubmit={handleSubmit}>
          <div className="mb-5">
            <label className="block mb-2 text-[13px] text-[var(--text-secondary)] font-medium">
              {t("createTag.nameLabel", "Tag Name")}
            </label>
            <input
              type="text"
              className="w-full px-3 py-2 border border-[var(--input-border)] rounded-md bg-[var(--input-bg)] text-[var(--text-primary)] text-sm hover:border-[var(--input-border-hover)] focus:outline-none focus:border-[var(--accent-blue)] focus:bg-[var(--input-bg-focus)]"
              value={tagName}
              onChange={(e) => setTagName(e.target.value)}
              placeholder={t("createTag.namePlaceholder", "Enter tag name...")}
              autoFocus
              maxLength={30}
            />
          </div>

          <div className="mb-5">
            <label className="block mb-2 text-[13px] text-[var(--text-secondary)] font-medium">
              {t("createTag.iconLabel", "Icon")} ({AVAILABLE_ICONS.length} available)
            </label>
            <div className="grid grid-cols-10 gap-1 max-h-[240px] overflow-y-auto p-1 bg-[var(--bg-tertiary)] rounded-lg border border-[var(--border-primary)]">
              {AVAILABLE_ICONS.map((iconOption) => {
                const IconComponent = iconOption.component;
                return (
                  <Button
                    key={iconOption.name}
                    type="button"
                    variant="unstyled"
                    className={`w-9 h-9 rounded-lg border-2 flex items-center justify-center cursor-pointer transition-all ${
                      selectedIcon === iconOption.name
                        ? "border-[var(--accent-blue)]"
                        : "border-transparent hover:bg-[var(--bg-quaternary)]"
                    }`}
                    onClick={() => setSelectedIcon(iconOption.name)}
                    style={
                      selectedIcon === iconOption.name
                        ? { borderColor: accentColor, backgroundColor: `${accentColor}20` }
                        : undefined
                    }
                    title={iconOption.displayName}
                  >
                    <IconComponent size={20} weight="regular" className="text-[var(--text-primary)]" />
                  </Button>
                );
              })}
            </div>
          </div>

          <div className="flex justify-center gap-2.5 mt-6">
            <Button
              type="button"
              variant="unstyled"
              className="px-5 py-2.5 text-[13px] font-medium rounded-lg border border-[var(--border-secondary)] bg-transparent text-[var(--text-secondary)] cursor-pointer transition-all hover:bg-[var(--bg-tertiary)]"
              onClick={onClose}
            >
              {t("createTag.cancel", "Cancel")}
            </Button>
            <Button
              type="submit"
              variant="unstyled"
              className="px-5 py-2.5 text-[13px] font-medium rounded-lg text-white cursor-pointer transition-all disabled:opacity-50 disabled:cursor-not-allowed"
              disabled={!tagName.trim()}
              style={{
                backgroundColor: tagName.trim() ? accentColor : "var(--bg-quaternary)",
                color: tagName.trim() ? "white" : "var(--text-tertiary)",
              }}
            >
              {t("createTag.create", "Create Tag")}
            </Button>
          </div>
        </form>
    </Modal>
  );
}
